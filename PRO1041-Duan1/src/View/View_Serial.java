    private void btn_addserialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addserialActionPerformed
        String ma = cbo_serial.getSelectedItem().toString();
        ChiTietSanPham ctsp = new ChiTietSanPham();
        ctsp.setId(idCTSP);
        ctsp.setIdSerial(serialService.getIdByMa(ma));
        ctspService.updateIdSerial(ctsp);

        Serial s = new Serial();
        s.setId(serialService.getIdByMa(ma));
        s.setTrangThai(1);
        serialService.updateTT(s);
        this.dispose();
    }//GEN-LAST:event_btn_addserialActionPerformed

