    private void fillTable(List<SanPham> list) {
        defaultTableModel = (DefaultTableModel) tbl_sanpham.getModel();
        defaultTableModel.setRowCount(0);
        for (SanPham sanPham : list) {
            defaultTableModel.addRow(new Object[]{sanPham.getId(), sanPham.getMa(), sanPham.getTen(),
                sanPham.getNgayTao(), sanPham.getNgayNhap(), sanPham.getTrangThai()});
        }
    }

    private SanPham getField() {
        SanPham sanPham = new SanPham();
        sanPham.setMa(txt_ma.getText().trim());
        sanPham.setTen(txt_ten.getText().trim());
        sanPham.setNgayTao(txt_ngayTao.getText().trim());
        sanPham.setNgayNhap(txt_ngayNhap.getText().trim());
        if (rdo_con.isSelected()) {
            sanPham.setTrangThai(1);
        } else {
            sanPham.setTrangThai(0);
        }

        return sanPham;
    }

