package ViewModel;

/**
 *
 * @author admin
 */
public class RoleModel {

    private String ma;

    private String role;

    public RoleModel() {
    }

    public RoleModel(String ma, String role) {
        this.ma = ma;
        this.role = role;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
    
}
