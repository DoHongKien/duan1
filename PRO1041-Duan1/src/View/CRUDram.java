    private void fillTable(List<Ram> list) {
        defaultTableModel = (DefaultTableModel) tblRam.getModel();
        defaultTableModel.setRowCount(0);
        for (Ram ram : list) {
            defaultTableModel.addRow(new Object[]{ram.getId(), ram.getMa(), ram.getTen(),
                ram.getNgayTao(), ram.getNgayNhap(), ram.getTrangThai()});
        }
    }

    private Ram getFieldInsert() {
        LocalDate date = LocalDate.now();
        Ram ram = new Ram();
        ram.setMa(txt_ma.getText().trim());
        ram.setTen(txtTen.getText().trim());
        ram.setNgayTao(date.format(DateTimeFormatter.ISO_DATE));
        ram.setNgayNhap(date.format(DateTimeFormatter.ISO_DATE));
        if (rdo_con.isSelected()) {
            ram.setTrangThai(1);
        } else {
            ram.setTrangThai(0);
        }

        return ram;
    }
    
    private Ram getFieldUpdate() {
        LocalDate date = LocalDate.now();
        Ram ram = new Ram();
        ram.setMa(txt_ma.getText().trim());
        ram.setTen(txtTen.getText().trim());
        ram.setNgayNhap(date.format(DateTimeFormatter.ISO_DATE));
        if (rdo_con.isSelected()) {
            ram.setTrangThai(1);
        } else {
            ram.setTrangThai(0);
        }

        return ram;
    }

